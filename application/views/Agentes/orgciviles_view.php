<section id="career" class="container">
	<p>Una organización no gubernamental (también conocida por las siglas ONG) es una entidad de carácter civil entendido como "El derecho y la disposición de participar en una comunidad, a través de la acción autorregulada, inclusiva, pacífica y responsable, con el objetivo de optimizar el bienestar público o social, con diferentes fines integrantes, creada independientemente de los gobiernos ya sea locales, regionales y nacionales, así como también de organismos internacionales. Jurídicamente adoptan diferentes estatus, tales como asociación, fundación, corporación y cooperativa, entre otras formas.</p>
	<p>Las ONG como su nombre lo indica: Organización no gubernamental, no depende de gobierno alguno y por ende será decisión de sus miembros si quieren o no optar por personería jurídica, los Estados y defensores jurídicos han querido crear una matriz de opinión induciendo a su registro lo cual contradice su propia naturaleza como ONG</p>
	<p>Una clasificación usada para los tipos de ONG, puede ser:</p>
	<p>Organizaciones voluntarias</p>
	<ol>
		<li>Agencias y organismos de servicios no lucrativos</li>
		<li>Organizaciones comunitarias o populares</li>
		<li>Organizaciones no gubernamentales para el desarrollo (ONGD)</li>
		<li>Organizaciones de inmigrantes</li>
	</ol>
