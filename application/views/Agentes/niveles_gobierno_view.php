	<p>En México el Poder Ejecutivo Federal del gobierno es ejercido por el Presidente de la República 
		cuya denominación oficial es <strong>Presidente Constitucional de los Estados Unidos Mexicanos</strong>, para llevar a cabo el desarrollo de sus atribuciones y funciones, el presidente tiene la facultad de nombrar libremente a los miembros de su gabinete, cada uno del cual es titular de una Secretaría de Estado que tiene a su cargo un ramo de la Administración Pública Federal, la organización de estas dependencias y las atribuciones que cada una tienen, están fijadas por la Ley Orgánica de la Administración Pública Federal.</p>
	<p>La <strong>Administración Pública</strong> Federal será centralizada y paraestatal conforme a la Ley 
		Orgánica que expida el Congreso, que distribuirá los negocios del orden administrativo de la Federación 
		que estarán a cargo de las Secretarías de Estado que constituirán el <strong>Gabinete en México</strong> 
		constituido por los titulares de las secretarías de estado. Los Secretarios de Estado serán nombrados por
		 el Presidente de los Estados Unidos Mexicanos. El <strong>Presidente de la República</strong> podrá convocar a reuniones de Secretarios de Estado y demás funcionarios competentes, cuando se trate de definir o evaluar la política del Gobierno Federal en materias que sean de la competencia concurrente de varias dependencias, o entidades de la Administración Pública Federal. Estas reuniones serán presididas por el Titular del Ejecutivo Federal y el Secretariado Técnico de las mismas estará adscrito a la Presidencia de la República</p>		
			
