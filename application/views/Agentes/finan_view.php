	      <h3>¿Qué es una incubadora de negocios?</h3>
	       <p>Son centros de apoyo que te ayudan a montar tu empresa. Las incubadoras se encargan de evaluar la viabilidad técnica, financiera y de mercado de tu proyecto empresarial, asi también te proporcionan servicios integrales de asesoría legal y administrativa, así como planes de mercadotecnia y ventas, e incluso espacio físico, equipo, logística y hasta acceso a financiamiento y capital semilla.</p>
	       <h4>Tipos de incubadora de negocios</h4>
	       <p>Existen incubadoras privadas, universitarias y gubernamentales, toda ellas avaladas por la Secretaría de Economía.</p>
			<p>Cada tipo de incubadora tiene diferentes estructuras y cada una tiene enfoques específicos.</p>		
