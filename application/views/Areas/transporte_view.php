    <h4>Es la movilidad eficiente, segura, equitativa, saludable, participativa y competitiva.</h4> 
    <p>Según la Real Academia Española, Movilidad es la capacidad de moverse o de recibir movimiento.</p>
	<p>La sostenibilidad es "satisfacer las necesidades de la generación actual sin comprometer la capacidad para satisfacer las necesidades de las generaciones futuras" Informe Brundtland (1987).</p>
	<p>La movilidad sostenible por tanto, es el modelo de movilidad que permite desplazarse con los mínimos impactos ambientales y territoriales.</p>
	<p>La World Business Council for Sustainable Development (WBCSD, la define como aquella que es capaz de satisfacer las necesidades de la sociedad de moverse libremente, acceder, comunicarse, comercializar o establecer relaciones sin sacrificar otros valores humanos o ecológicos básicos actuales o del futuro. Es decir, sin comprometer el bienestar de las generaciones futuras.</p>
	<p>La movilidad sostenible engloba a un conjunto de procesos y acciones orientados para conseguir como objetivo final un uso racional de los medios de transporte por parte tanto de los particulares como de los profesionales.</p>
	<p>La movilidad sostenible es más equitativa porque garantiza el acceso universal de todos los ciudadanos a todos los lugares públicos y equipamientos en transporte públicos colectivo o en medios no motorizados, e intenta evitar la exclusión laboral relacionada con la falta de vehículo privado a motor. </p>
    
