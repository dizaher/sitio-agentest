 <h4>Energía alternativa para autos</h4>
 <ul>
	<li>Tecnología solar</li>
</ul>
<p>Estos vehículos se componen de un motor eléctrico que se alimenta con energía solar gracias a paneles solares, ubicados por lo general en la superficie del automóvil. Estos paneles están compuestos de celdas fotovoltaicas, que en resumen son las encargadas de convertir la energía del sol en electricidad. Toda la electricidad adquirida puede ser almacenada en baterías, las cuales pueden ser de plomo, níquel-cadmio y litio, o directamente en los motores.</p>
<p>Los beneficios están en que estos vehículos utilizan el recurso natural más grande del mundo, pero el diseño de los paneles no ha permitido que sean muy cómodos y aerodinámicos, y sus componentes siguen siendo muy frágiles. Pero la industria automotriz, apoyada por entidades gubernamentales, está trabajando arduamente en esto y por eso promueve concursos en universidades como el World Solar Challenge y el American Solar Challenge.</p>
<ul>
	<li>Hidrógeno</li>
</ul>
<p>El hidrógeno diatómico es una fuente primaria utilizada para la propulsión de vehículos, ya sea como el elemento esencial para la combustión de los motores o alimentando los motores eléctricos, en los que se convierte en electricidad, alimenta una pila y mueve los motores eléctricos. En el caso de los motores de explosión, el hidrógeno funciona de manera similar a la gasolina, quemándose para producir movimiento.</p>
<p>Este es uno de los inventos que más han sido utilizados por los fabricantes de automóviles para sus investigaciones, pues no emite gases contaminantes, aunque hasta el momento han sido pocos los que han utilizado el agua como la única energía alternativa para sus conceptos.</p>
<ul>
	<li>La electricidad</li>
</ul>
<p>Los motores eléctricos generalmente están acompañados por otros motores encargados de cargar su batería y producirles la energía necesaria para mover los vehículos. Es decir, estos motores necesitan de un sistema que permita convertir la energía de las llantas en electricidad o un motor extra que proporcione la carga del motor eléctrico.</p>
<p>El motor eléctrico fue uno de los primeros motores que se fabricaron en la historia del automóvil, a principios de 1830, por investigadores europeos como Robert Anderson y el profesor Sibrandus Stratingh, pero solo en los últimos años se ha convertido en una fuente alternativa viable y real, traducida en una nueva categoría de vehículos, la de eléctricos e híbridos.</p>
<ul>
	<li>Etanol o Metanol</li>
</ul>
<p>El etanol es un compuesto químico que se consigue a través de la fermentación del azúcar, para lo cual es muy utilizada la caña de azúcar, aunque ya se está tratando satisfactoriamente con otros ‘ingredientes’ como la remolacha. El metanol, por su parte se fabrica a partir del metano, principal componente del gas natural, pero no ha sido tan exitoso como el primero.</p>
<p>Sistema de combustión alternativa para los vehículos, que simplemente consiste en combinar en un motor de cuatro tiempos normal la gasolina corriente con  otro combustible. En este caso, el combustible alternativo que más se utiliza, pues sus propiedades con el medio ambiente y su comportamiento en el motor son muy similares a las de la gasolina, es el alcohol (metanol o etanol).</p>
<ul>
	<li>Gas natural</li>
</ul>
<p>Se trata de una adaptación que se le hace al motor, la cual le permite funcionar con gas natural vehicular, almacenado en cilindros. Lo más beneficioso es que este es un sistema que se puede combinar con el de gasolina en el mismo motor de cuatro tiempos, cambiándolo manual o automáticamente con un switch en la consola del carro.</p>
<p>Además de los beneficios para el medio ambiente, el gas natural vehicular es mucho más económico que la gasolina. Por ello, los vehículos de transporte público, que son los que más kilómetros recorren cada día y perciben los mayores beneficios en el ahorro de costos de combustible, son los que más han acogido este sistema.</p>
<p>Su mayor problema es que requiere de una instalación muy cuidadosa y original, pues puede dañar el motor y otras partes del vehículo. Además, el cilindro de gas es pesado y el sistema le resta algunos caballos de potencia al motor. De todos modos, ya se ha popularizado y algunos fabricantes de vehículos han empezado a vender algunos de sus modelos con adaptaciones de gas natural vehicular.</p>
<ul>
	<li>Propano</li>
</ul>
<p>El propano – también conocido como gas licuado de petróleo  – es un subproducto del procesamiento natural del gas natural y la refinación de petróleo. Ampliamente utilizado para cocinar y calentar, el propano es también un combustible alternativo popular para vehículos. </p>
<p>Positivo: El propano  produce menos emisiones que la gasolina y también existe una gran red de transporte, almacenamiento y distribución para este producto. </p>
<p>Negativo: La producción de gas natural crea metano,  un gas de invernadero que es 21 veces peor que el CO2 para el calentamiento global.</p>
<ul>
	<li>Biodiesel</li>
</ul>
<p>El biodiesel es un combustible alternativo basado en grasas vegetales o animales, aún aquellas recicladas de restaurantes que las han usado para cocinar. Los motores de vehículos pueden ser convertidos a quema de biodiesel en su forma más pura, y este también puede ser mezclado con diesel y usado en motores no modificados. </p>
<p>Positivo: El biodiesel es seguro, biodegradable, reduce los contaminantes del aire asociado a las emisiones de vehículos, tales como micropartículas, monóxido de carbono e hidrocarburos. </p>
<ul>
	<li>Combustibles  P Serie</li>
</ul>
<p>Los combustibles P Serie son una mezcla de etanol, líquidos del gas natural y metiltetrahidrofurano, un solvente derivado de biomasa. Los combustibles P Serie son combustibles alternativos claros y de alto octanaje que puede ser usado en vehículos flexibles. Positivo: Los combustibles P Serie pueden ser usados solos o mezclados con gasolina en cualquier proporción al adicionarlo simplemente en el tanque. </p>

