      <h4>Mensaje al inversionista</h4>     
      <p>El territorio mexicano presenta una radiación solar promedio superior a 5 kWh por m2 al día, lo que coloca a México como una de las zonas con el mejor nivel en el mundo.</p>
      <p>México es uno de los principales productores de energía a partir de recursos geotérmicos. A febrero de 2012, el país ocupó la 4ta posición con una participación global de 8.7%, solo detrás de EUA (28.7%), Filipinas (17.3%) e Indonesia (10.9%).</p>
      <p>A febrero de 2012, México ocupó el 2° lugar en proyectos aprobados por la Junta del Mecanismo de Desarrollo Limpio, con una participación global del 3.51%.</p>
      <p>México forma parte del llamado “cinturón solar” que lo ubica entre los principales países con mayor potencial solar en el mundo.</p>
      <p>Para incentivar la participación privada en la generación de energía eléctrica a través de fuentes renovables, la Comisión Reguladora de Energía (CRE) otorga permisos bajo diferentes esquemas, entre los que destacan:</p>
      <ul>
        <li>Autoabastecimiento: generación de energía eléctrica para autoconsumo de personas físicas y morales.</li>
        <li>Productor Independiente de Energía: generación de energía eléctrica en una planta mayor a 30 MW para venta exclusiva a la Comisión Federal de Electricidad (CFE).</li>
      </ul>
      <p>Otras de las modalidades para la generación de energía eléctrica son cogeneración, pequeña producción, exportación e importación.</p>
      <p>Los incentivos de la CFE para la generación de energía eléctrica privada son:</p>
      <ul>
        <li>Banco de energía: permite la acumulación de excedentes de energía a los productores bajo en el esquema de autoabastecimiento para ser utilizados en el futuro o ser vendidos a la CFE.</li>
        <li>Tarifa preferencial para la transmisión de energía: Cargo por servicio de transmisión para energías renovables o cogeneración eficiente de $0.14 pesos/kWh, en lugar de $0.30-$0.40 pesos/kWh por fuentes tradicionales.</li>
      </ul>
      <p>México es un país seguro para la inversión extranjera. El país cuenta con 28 APPRIs (Acuerdos para la Promoción y Protección Recíproca de las Inversiones) y acuerdos para evitar la doble tributación con más de 40 países.</p>                         

