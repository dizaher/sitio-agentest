      <h4>Tipos de energía</h4>
      <p>Eólica: Es la transformación la energía cinética del viento en energía mecánica o eléctrica.</p>
      <p>Solar: La radiación emitida por el sol:</p>
      <ol>
        <li>Fotovoltaica: Es la transformación de la radiación solar en electricidad.</li>
        <li>Termosolar: Es el aprovechamiento de la radiación solar para la captación y almacenamiento de calor.</li>
      </ol>
      <p>Hidráulica: La energía potencial o cinética del agua transformada en electricidad.</p>
      <p>Geotérmica: Energía térmica producida en el interior de la tierra.</p>
      <p>Biomasa: Se obtiene a través de residuos animales y vegetales.</p>
