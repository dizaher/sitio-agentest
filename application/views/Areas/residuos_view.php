    <p>Son los generados en las casas, como resultado de la eliminación de los materiales que se utilizan en las actividades domésticas; son también los que provienen de establecimientos o la vía pública, o los que resultan de la limpieza de las vías o lugares públicos y que tienen características como los domiciliarios. Su manejo y control es competencia de las autoridades municipales y delegacionales.</p>
	<p>Tienen también la consideración de residuos urbanos:</p>
	<ul>
		<li>Residuos procedentes de la limpieza de vías públicas, zonas verdes, áreas recreativas y playas.</li>
		<li>Animales domésticos muertos, así como muebles, enseres y vehículos abandonados.</li>
		<li>Residuos y escombros procedentes de obras menores de construcción y reparación domiciliaria.</li>
	</ul>
	
	<p>Según datos del INEGI el 93% de los municipios y delegaciones del país cuentan con servicios de recolección y disposición de residuos; de éstos, el 6% da tratamiento al menos a una parte de los residuos recolectados.</p>
	<p>En México se recolecta cada día, en promedio, 86 mil 343 toneladas de residuos sólidos urbanos, que son los generados en las viviendas, parques, jardines y edificios públicos, principalmente.</p>
	<p>En cinco entidades se recoge casi la mitad de los residuos del país. El Distrito Federal registra la mayor proporción con 19.6% del total nacional; le siguen el estado de México con 9.6%; Jalisco con 7.6%: Veracruz de Ignacio de la Llave, con 5.2% y Guanajuato con 4.3%.</p>
	<p>En 50 municipios y delegaciones, donde reside 32.2% de la población, se concentra el 50.6% de la recolección. Cabe destacar que tres delegaciones del Distrito Federal explican el 10% de la recolección total nacional.</p>
	<p>En nuestro país, únicamente el 11% de los residuos sólidos recolectados son separados o segregados desde su origen, el resto son recogidos sin selección alguna.</p>			
