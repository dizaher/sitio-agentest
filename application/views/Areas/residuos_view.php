        <h4>Composición de los residuos sólidos</h4>
		<p>Los residuos sólidos urbanos están compuestos de los siguientes materiales:</p>
		<ul>
			<li>Vidrio. Son los envases de cristal, frascos, botellas, etc.</li>
			<li>Papel y cartón. Periódicos, revistas, embalajes de cartón, envases de papel, cartón, etc.</li>
			<li>Restos orgánicos. Son los restos de comida, de jardinería, etc. En peso son la fracción mayoritaria en el conjunto de los residuos urbanos.</li>
			<li>Plásticos. En forma de envases y elementos de otra naturaleza.</li>
			<li>Textiles. Ropas y vestidos y elementos decorativos del hogar.</li>
			<li>Metales. Son latas, restos de herramientas, utensilios de cocina, mobiliario etc.</li>
			<li>Madera. En forma de muebles mayoritariamente.</li>
		</ul>
		<p>Los RSU tienen como principal problemática el incremento exponencial de su volumen debido a:</p>
		<ul>
			<li>El aumento progresivo de la población y su concentración en determinadas áreas.</li>
			<li>Crecimiento progresivo de la generación per cápita de residuos.</li>
			<li>Escasos programas educativos a la comunidad sobre la temática.</li>
			<li>Sistemas de tratamiento y/o disposición final inadecuados/inexistentes.</li>
			<li>Falta de una evaluación integral de costos y asignación de recursos.</li>
			<li>El uso de envases sin retorno (fabricados con materiales no degradables).</li>
		</ul>
     </div>
