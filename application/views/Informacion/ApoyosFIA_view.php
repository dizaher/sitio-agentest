        <h3>Apoyos para las I+D+i en México</h3>
        <p>Fomentar la inversión del sector productivo en actividades de investigación y desarrollo tecnológico que impacten en la competitividad de las empresas en México; estimular la generación de nuevos empleos de calidad, e impulsar el crecimiento económico del país son los objetivos de los nuevos Programas de Estímulo a la Investigación, Desarrollo Tecnológico e Innovación.</p>
		<p>El Consejo Nacional de Ciencia y Tecnología, (Conacyt) y la Secretaría de Economía, fomentan tres nuevos programas para apoyo a las I + D + i en México.</p>
		<strong>Los programas de Estímulo a la Investigación, Desarrollo Tecnológico e Innovación son:</strong>
		<ul>
			<li>Innovación Tecnológica para Negocios de Alto Valor Agregado (INNOVAPYMES)</li>
		</ul>
		<p>Cuenta con un monto de 600 millones de pesos y su objetivo es incentivar la inversión en investigación y desarrollo tecnológico a las Mipymes, mediante el otorgamiento de estímulos económicos complementarios a las empresas que realicen actividades relacionadas a la investigación y desarrollo tecnológico, con la finalidad de incrementar su competitividad, la creación de nuevos empleos de calidad e impulsar el crecimiento económico del país. Está dirigido a micro, pequeñas y medianas empresas (Mipymes).</p>
		<ul>
			<li>Desarrollo e Innovación en Tecnologías Precursoras (PROINNOVA) Vinculación, Pymes</li>
		</ul>
		<p>Su bolsa es de 700 millones de pesos y pretende incentivar la inversión en investigación y desarrollo tecnológico de las empresas, mediante el otorgamiento de estímulos económicos complementarios para aquellas que realicen actividades relacionadas con la investigación y el desarrollo tecnológico en colaboración con centros educativos y de investigación, de forma que los apoyos tengan el mayor impacto posible sobre la competitividad de la economía.</p>
		<p>Los destinatarios de este fondo son consorcios, es decir, uniones entre empresas, instituciones de educación superior y centros de investigación.</p>
		<ul>
			<li>Innovación Tecnológica para la Competitividad de las Empresas (INNOVATEC)</li>
		</ul>
		<p>Es el fondo que cuenta con la mayor cantidad de recursos: mil 200 millones de pesos. El objetivo de éste es incentivar la inversión en investigación y desarrollo tecnológico (IDT) de las empresas, mediante el otorgamiento de estímulos económicos complementarios a las que realicen actividades relacionadas con la investigación y desarrollo tecnológico, con la finalidad de incrementar su competitividad, la creación de nuevos empleos de calidad e impulsar el crecimiento económico del país.</p>

