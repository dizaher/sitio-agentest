        <h3>Fondos y apoyos CONACYT</h3>
        <h4>Fondos sectoriales</h4>
        <ul>
			<li>Los Fondos Sectoriales</li>
		</ul>
		<p>Son fideicomisos que las dependencias y las entidades de la Administración Pública Federal conjuntamente con el CONACYT pueden constituir para destinar recursos a la investigación científica y al desarrollo tecnológico en el ámbito sectorial correspondiente.</p>
		<ul>
			<li>Fondos mixtos</li>
		</ul>
		<p>Los Fondos Mixtos son un instrumento que apoya el desarrollo científico y tecnológico estatal y municipal, a través de un Fideicomiso constituido con aportaciones del Gobierno del Estado o Municipio, y el Gobierno Federal, a través del Consejo Nacional de Ciencia y Tecnología.</p>
		<ul>
			<li>Fondos institucionales</li>
		</ul>
		<p>Constituido por tres fondos:</p>
		<p>Fondo institucional del CONACyT (FOINS). Es uno de los tres Fondos Institucionales constituidos por el CONACYT, con base en los artículos 23 y 24 de la Ley de Ciencia y Tecnología, cuya operación técnica y operativa está a cargo de las diferentes áreas sustantivas del CONACYT; y fue creado para dar respuesta a la demandas de apoyos que realiza la comunidad científica y tecnológica del país al CONACYT.</p>
		<p>Fondo institucional de Fomento Regional para el Desarrollo Científico, Tecnológico y de Innovación (FORDECYT). Es un Programa del CONACYT que busca coadyuvar al desarrollo económico y social de las regiones del país mediante el financiamiento a propuestas de investigación, desarrollo e innovación tecnológica de alto impacto potencial que aporten soluciones a los problemas y necesidades que limitan el desarrollo o que generen oportunidades de mejora.</p>
		<p>Fondo para el Fomento y Apoyo para la Investigación Científica y Tecnológica en Bioseguridad y Biotecnología. Es el Consejo Nacional de Ciencia y Tecnología mantiene un índice con revistas científicas y tecnológicas como reconocimiento a su calidad y excelencia editorial.</p>
		<ul>
			<li>Información de fondos y fideicomisos CONACyT</li>
			<li>Programa de estímulos a la innovación </li>
		</ul>
		<p>Es el programa de apoyo para las empresas que invierten en proyectos de investigación, desarrollo de tecnología e innovación dirigida al desarrollo de nuevos productos, procesos o servicios.</p>
		<ul>
			<li>Programa AVANCE</li>
		</ul>
		<p>Es un programa creado para impulsar la identificación de oportunidades y creación de negocios basados en la explotación de desarrollos científicos y/o desarrollos tecnológicos.</p>
