        <h3>Cadena Productiva de Materiales</h3>
        <p>Este estudio tiene el objetivo de analizar aspectos del proceso de extracción, clasificación, producción y comercialización de ciertos materiales, específicamente de metales, polímeros y orgánicos en México, los que están dirigidos especialmente al mercado de la construcción. Para su realización se tomó en cuenta la guía de las cadenas productivas de los minerales, elaborada en 2010 por la propia Dirección de Fomento y  Organización. Se consideró pertinente abordar el análisis desde la perspectiva de mini cadena productiva.</p>
		<p>Secciones del Documento:</p>
		<ul>
			<li><p>Primera: Precisa el objetivo y define los alcances del análisis</p></li>
			<li><p>Segunda: Plantea el grado de interacción existente entre los participantes que integran la cadena, destacando particularmente, su nivel de organización y las características del mercado (oferta, demanda, precios, comercialización).</p>
				<p>Además, con base a los procedimientos planteados por Damien van der Heyden y Patricia Camacho en su Guía Metodológica, y buscando que el análisis de la información se desarrollara desde una perspectiva ordenada y sistemática, se desglosó la segunda etapa en cinco bloques temáticos, a saber: historia, entorno, participantes, relaciones-organizaciones y mercado.</p>
			</li>
			<li><p>Tercera: Proporciona herramientas prácticas para inspeccionar la información vertida en la segunda sección</p></li>
		</ul>
		<p>El estudio, en su parte concluyente, persigue un propósito dual:</p>
		<ol>
			<li>Identificar puntos críticos y posibles ventajas competitivas de la cadena a través de la elaboración de una matriz FODA (fortalezas, oportunidades, debilidades y amenazas); y</li>
			<li>Proponer estrategias de acción tendientes a mejorar las condiciones actuales de operación de la cadena productiva de estos materiales </li>
		</ol>
        
