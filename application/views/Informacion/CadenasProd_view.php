        <h3>Cadena Productiva “Sistema Multiagente Embebido”(SME)</h3>
        <p>Una <strong>Cadena productiva</strong> es el conjunto de agentes y actividades económicas que intervienen en un proceso productivo, desde la provisión de insumos y materias primas, su transformación y producción de bienes intermedios y finales, y su comercialización en los mercados internos y externos.</p>
		<p>En este documento encontramos el desarrollo la Cadena Productiva del SME que comprende desde las materias primas para la fabricación de cada componente electrónico, hasta el producto terminado en este caso una placa de desarrollo electrónico multipropósito (Arduino) que es la parte principal y nos servirá para el desarrollo del SME. En esta cadena estamos incluyendo al Smartphone pues forma parte del SME y también forma parte de la cadena productiva de electrónicos.</p>
		<p>La información de la Cadena Productiva fue tomada de la página oficial del SIEM (Sistema de Información Empresarial Mexicano, www.siem.gob.mx) en el apartado de Cadenas Productivas, a esta se le han hecho algunas modificaciones ya que la cadena completa es muy extensa y para nuestros fines hay puntos en los cuales no incursionamos, por tal motivo se modificaron de acuerdo a la necesidad de la empresa.</p>

